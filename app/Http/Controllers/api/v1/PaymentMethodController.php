        try{
			$payment_method = Payment_method::all();
			if($payment_method->isEmpty()){
				return $this->sendError('No payment method found');
			}
			$data['payment_methods'] 	= $payment_method;
			$data['logopath'] 	= asset('/uploads/payment_methods/');
			return $this->sendResponse($data, 'All payment methods.');
		}
        catch (\Throwable $th) {
            return response()->json(['success' => false, 'message' => 'Payment Methods Not found', 'errors' => $th->getMessage()]);
        }
    }

